package scratch;

public class Point {
    public int x;
    public int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return String.format("(%d, %d)", x, y);
    }

    @Override
    public boolean equals(Object o) {
        if (o == null) {
            return false;
        }

        if (this == o) {
            return true;
        }

//        if (!(this.getClass() == o.getClass())) {
//            return false;
//        }
        if (!(o instanceof Point)) {
            return false;
        }

        Point other = (Point) o;
        return this.x == other.x && this.y == other.y;
    }
}
